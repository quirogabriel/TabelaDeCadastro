    private RegistroCliente lerDados() {
        RegistroCliente r = new RegistroCliente();
        r.setId(new Integer(txtId.getText()));
        r.setNome(txtNome.getText());
        r.setEndereço(txtEndereço.getText());
        r.setTelefone(new Integer(txtTelefone.getText()));
        r.setEmail(txtEmail.getText());
        r.setStatus(Boolean.valueOf(txtStatus.getText()));
        return r;
    }

    private void carregarDados(int linha) {
        txtId.setText(
                tblRegistroCliente.getValueAt(linha, 0).toString());
        txtNome.setText(
                tblRegistroCliente.getValueAt(linha, 1).toString());
        txtEndereço.setText(
                tblRegistroCliente.getValueAt(linha, 2).toString());
        txtTelefone.setText(
                tblRegistroCliente.getValueAt(linha, 3).toString());
        txtEmail.setText(
                tblRegistroCliente.getValueAt(linha, 4).toString());
        txtStatus.setText(
                tblRegistroCliente.getValueAt(linha, 5).toString());
    }

    private void limparDados() {
        txtId.setText("");
        txtNome.setText("");
        txtEndereço.setText("");
        txtTelefone.setText("");
        txtEmail.setText("");
        txtStatus.setText("");
    }

    private void preencherGrid() {
        List<RegistroCliente> dados = RegistroClienteControle.obterTodos();
        DefaultTableModel dtm = new DefaultTableModel(
                new String[]{"Id", "Nome", "Endereço", "Telefone", "Email", "Status"}, 0);
        dados.forEach((p) -> {
            dtm.addRow(new Object[]{p.getId(), p.getNome(), p.getEndereço(), p.getTelefone(), p.getEmail(), p.getStatus()});
        });
        tblRegistroCliente.setModel(dtm);
    }

