    private void Incluir(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Incluir
        RegistroCliente r = lerDados();
        try {
            RegistroClienteControle.incluir(r);
            limparDados();
            JOptionPane.showMessageDialog(this,
                    "Incluisão efetuada",
                    "Incluir", JOptionPane.INFORMATION_MESSAGE);
            preencherGrid();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Erro na inclusão: " + ex.getMessage(),
                    "Incluir", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_Incluir

    private void Excluir(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Excluir
        RegistroCliente r = lerDados();
        try {
            RegistroClienteControle.excluir(r.getId());
            limparDados();
            JOptionPane.showMessageDialog(this,
                    "Exclusão efetuada",
                    "Excluir", JOptionPane.INFORMATION_MESSAGE);
            preencherGrid();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Erro na exclusão: " + ex.getMessage(),
                    "Excluir", JOptionPane.ERROR_MESSAGE);
        }    }//GEN-LAST:event_Excluir

    private void Alterar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Alterar
        RegistroCliente r = lerDados();
        
        try {
            RegistroClienteControle.alterar(r.getId(),r);
            limparDados();
            JOptionPane.showMessageDialog(this,
                    "Alteração efetuada",
                    "Alterar", JOptionPane.INFORMATION_MESSAGE);
            preencherGrid();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Erro na alteração: " + ex.getMessage(),
                    "Alterar", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_Alterar

